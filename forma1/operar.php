<?php   
    class Operar {
        
        public function sumar($n1, $n2){

            return $n1+$n2;

        }

        public function restar($n1, $n2){

            return $n1-$n2;

        }

        public function multiplicar($n1,$n2){

            return $n1*$n2;

        }

        public function dividir($n1,$n2){

            return $n1/$n2;
            
        }
}
    